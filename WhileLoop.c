// C program to demonstrate while loop
#include <stdio.h>
int main()
{
    // Intialization of loop variable
    int i = 0;
    // test expression as (i < 5), means the loop will execute till i is less than 5
    while (i < 5) {
        // loop statements
        printf("Curry\n");
        // updating the loop variable
        i++;
    }
    return 0;
}